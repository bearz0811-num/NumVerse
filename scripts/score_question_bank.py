def round10(x: float) -> int:
    return int(round(x / 10.0) * 10)


def main() -> None:
    data = json.loads(BANK.read_text(encoding="utf-8"))
    for q in data["questions"]:
        tier = TIER_MAP[q["knowledgePoint"]]
        df = DIFF_FACTOR[q["difficulty"]]
        kf = TIER_FACTOR[tier]
        base_value = max(120, min(450, round10(BASE * df * kf)))
        q["baseValue"] = base_value
        q["scoreMeta"] = {
            "difficulty": q["difficulty"],
            "difficultyFactor": df,
            "knowledgeTier": tier,
            "knowledgeFactor": kf,
        }

    data["version"] = 2
    data["scoreRule"] = {
        "formula": "round10(120 * difficultyFactor * knowledgeFactor)",
        "difficultyFactor": DIFF_FACTOR,
        "knowledgeFactor": {str(k): v for k, v in TIER_FACTOR.items()},
        "tiers": {
            "1": "定義/辨識",
            "2": "程序/套公式",
            "3": "標準解題/典型應用",
            "4": "綜合/建模",
            "5": "證明/高認知",
        },
    }
    BANK.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Scored {len(data['questions'])} questions -> {BANK}")


if __name__ == "__main__":
    main()
