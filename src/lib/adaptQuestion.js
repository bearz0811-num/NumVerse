function rewriteOptions(options, replacements) {
  if (!options) return null
  return options.map((option) => ({
    ...option,
    text: replacements?.[option.letter] ?? option.text,
  }))
}

/**
 * @param {object} question
 * @param {object} sceneAdapt
 * @param {{ playerName?: string }} [ctx]
 */
export function adaptQuestion(question, sceneAdapt, _ctx = {}) {
  if (!question) return null

  const sceneId = sceneAdapt?.sceneId
  const rewrite =
    sceneAdapt?.rewrites?.[question.id] ??
    sceneAdapt?.rewrites?.[String(question.id)] ??
    SCENE_QUESTION_REWRITES[sceneId]?.[question.id]
  const displayQuestion = rewrite?.question ?? question.question
  const displayOptions = rewriteOptions(question.options, rewrite?.options)

  // 改寫過的題目：原圖上的文字／情境對不上新敘事，直接不顯示
  const keepImage = Boolean(rewrite?.preserveImage)
  const displayImage = rewrite && !keepImage ? null : question.image ?? null
  const displayImageAlt =
    rewrite && !keepImage
      ? null
      : rewrite?.imageAlt ?? question.imageAlt ?? null

  return {
    ...question,
    adapted: true,
    displayQuestion,
    displayOptions,
    displayImage,
    displayImageAlt,
    rawQuestion: question.question,
  }
}

